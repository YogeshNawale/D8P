package com.loops;

public class DisplaySeries3 {

	public static void main(String[] args) {
		// Write a program in Java to display the first 10 terms of the following series:
		// 10, 20, 30, 40, ……..
		
		int i,result;
		for (i=1; i<=10; i++)
			{
			result=i*10;
			System.out.print(result+"  ");
			}
		

	}

}
